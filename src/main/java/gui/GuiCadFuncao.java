    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        if (alterando) resetarTela();
        else {
            try {
                funcao = (Funcao) tblFuncoes.getValueAt(tblFuncoes.getSelectedRow(), 0);
                String descricaoFuncao = funcao.getDescricaoFuncao();
                txtDescricao.setText(descricaoFuncao);
                txtDescricao.requestFocus();
                txtDescricao.selectAll();
                alterando = true;
                btnAlterar.setText("Alterando");
                btnAlterar.setSelected(true);
                tblFuncoes.setEnabled(false);
            } catch (Exception headlessException) {
                JOptionPane.showMessageDialog(null, "Por favor, selecione uma função.");
                btnAlterar.setSelected(false);
            }
        }
    }//GEN-LAST:event_btnAlterarActionPerformed
    
    public void preencherTabela() {
        DefaultTableModel tbl = (DefaultTableModel) tblFuncoes.getModel();
        tblFuncoes.setRowSorter(new TableRowSorter(tbl));

        int qtdLinhas = tbl.getRowCount();
        for (int i = qtdLinhas - 1; i >= 0; i--) {
            tbl.removeRow(0);
        }

        List<Funcao> funcoes = new ArrayList();
        try {
           funcoes = dadosFuncao.getList();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Nenhuma função encontrada!", "Cadastro de Função", JOptionPane.ERROR_MESSAGE);
        }
        
        for (int i = 0; i < funcoes.size(); i++) {
            Funcao func = (Funcao) funcoes.get(i);
            Object linha [] = {func};
            tbl.addRow(linha);
        }

        resetarTela();
    }
    
    public void resetarTela() {
        txtDescricao.setText("");
        txtDescricao.requestFocus();
        btnAlterar.setEnabled(true);
        btnAlterar.setSelected(false);
        btnAlterar.setText("Alterar");
        tblFuncoes.setEnabled(true);
    }
    
    public boolean validarCampos() {
        if(!(txtDescricao.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a Descrição da Função.");
            txtDescricao.requestFocus();
            return false;
        }
        return true;
    }

