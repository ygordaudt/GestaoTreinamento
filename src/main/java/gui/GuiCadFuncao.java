    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if (validarCampos()) {
            if (!alterando) {
                String descricaoFuncao = txtDescricao.getText();
                funcao = new Funcao (descricaoFuncao);
                try {
                    dadosFuncao.incluir(funcao);
                    JOptionPane.showMessageDialog(null, "Função cadastrada com sucesso!", "Cadastro de Função", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Cadastro de Função", JOptionPane.ERROR_MESSAGE);
                }
                
            } else {
                funcao = (Funcao) tblFuncoes.getValueAt(tblFuncoes.getSelectedRow(), 0);
                funcao.setDescricaoFuncao(txtDescricao.getText());
                try {
                    dadosFuncao.alterar();
                    JOptionPane.showMessageDialog(null, "Cadastro alterado com sucesso!", "Cadastro de Função", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Cadastro de Função", JOptionPane.ERROR_MESSAGE);
                }
                
                try {
                    alunos = dadosAluno.getList();
                } catch (Exception ex) {
                    Logger.getLogger(GuiCadFuncao.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                for (Aluno al : alunos) {
                    if (al.getFuncaoColaborador().equals(funcao)) {
                        al.setFuncaoColaborador(funcao);
                    }
                }
                
                try {
                    dadosAluno.alterar();
                } catch (Exception ex) {
                    Logger.getLogger(GuiCadFuncao.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                
            }
            
            preencherTabela();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

