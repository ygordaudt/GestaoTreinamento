    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        if (validarCampos()) {
            if (!alterando) {
                String descricao = txtDescricao.getText();
                CorEquipe ce = new CorEquipe(descricao);
                try {
                    dadosCorEquipe.incluir(ce);
                    JOptionPane.showMessageDialog(null, "Cor cadastrada com sucesso!", "Cadastro de Cores", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Cadastro de Cores", JOptionPane.ERROR_MESSAGE);
                }
                
            } else {
                corEquipe = (CorEquipe) tblCores.getValueAt(tblCores.getSelectedRow(), 0);
                corEquipe.setDescricao(txtDescricao.getText());
                try {
                    dadosCorEquipe.alterar();
                    //dadosEquipe.alterar();
                    JOptionPane.showMessageDialog(null, "Cadastro alterado com sucesso!", "Cadastro de Cores", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Cadastro de Cores", JOptionPane.ERROR_MESSAGE);
                }
            }
            
            preencherTabela();
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        try {
            this.corEquipe = (CorEquipe) tblCores.getValueAt(tblCores.getSelectedRow(), 0);
            
            int opcao
            = JOptionPane.showOptionDialog(null,
                "Confirma a exclusão?",
                "Pergunta",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE, null,
                new String[]{"Sim", "Não"}, "Sim");
            
            if (opcao == 0) {
            dadosCorEquipe.remover(this.corEquipe);
            }
            
            preencherTabela();
            JOptionPane.showMessageDialog(rootPane, "Cor removida com sucesso.");
            
        } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Por favor, selecione uma cor.");

        }

    }//GEN-LAST:event_btnRemoverActionPerformed

