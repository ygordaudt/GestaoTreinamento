    public void preencherTabela() {
        DefaultTableModel tbl = (DefaultTableModel) tblCores.getModel();
        tblCores.setRowSorter(new TableRowSorter(tbl));

        int qtdLinhas = tbl.getRowCount();
        for (int i = qtdLinhas - 1; i >= 0; i--) {
            tbl.removeRow(0);
        }
        
        List<CorEquipe> ces = new ArrayList<>();
        try {
            ces = dadosCorEquipe.getList();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nenhuma cor encontrada!", "Cadastro de Cores", JOptionPane.ERROR_MESSAGE);
        }
        
        for (int i = 0; i < ces.size(); i++) {
            CorEquipe ce = (CorEquipe) ces.get(i);
            Object linha [] = {ce, ce.getDescricao()};
            tbl.addRow(linha);
        }

        resetarTela();
    }
    
    public void resetarTela() {
        txtDescricao.setText("");
        txtDescricao.requestFocus();
        btnAlterar.setEnabled(true);
        btnAlterar.setSelected(false);
        btnAlterar.setText("Alterar");
        tblCores.setEnabled(true);
        alterando = false;
    }
    
    public boolean validarCampos() {
        if(!(txtDescricao.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a descrição da cor.");
            txtDescricao.requestFocus();
            return false;
        }
        return true;
    }

