    public void preencherTabelaContextoInfracao() {
        DefaultTableModel tbl = (DefaultTableModel) tblContextoInfracao.getModel();
        tblContextoInfracao.setRowSorter(new TableRowSorter(tbl));

        int qtdLinhas = tbl.getRowCount();
        for (int i = qtdLinhas - 1; i >= 0; i--) {
            tbl.removeRow(0);
        }

        List<ContextoInfracao> contextosInfracao = new ArrayList();
        try {
           contextosInfracao = dadosContextosInfracao.getList();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Nenhum contexto encontrado!", "Cadastro de Infração", JOptionPane.ERROR_MESSAGE);
        }
        
        for (int i = 0; i < contextosInfracao.size(); i++) {
            ContextoInfracao contexto = (ContextoInfracao) contextosInfracao.get(i);
            Object linha [] = {contexto, contexto.getPontuacao()};
            tbl.addRow(linha);
        }

        resetarTelaContextoInfracao();
    }
    
    public void preencherTabelaTipoInfracao() {
        DefaultTableModel tbl = (DefaultTableModel) tblTipoInfracao.getModel();
        tblTipoInfracao.setRowSorter(new TableRowSorter(tbl));

        int qtdLinhas = tbl.getRowCount();
        for (int i = qtdLinhas - 1; i >= 0; i--) {
            tbl.removeRow(0);
        }
        
        List<TipoInfracao> tiposInfracao = new ArrayList<>();
        try {
            tiposInfracao = dadosTipoInfracao.getList();
        } catch (Exception ex) {
            Logger.getLogger(GuiCadInfracao.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        for (int i = 0; i < tiposInfracao.size(); i++) {
            TipoInfracao tipoInfracao = (TipoInfracao) tiposInfracao.get(i);
            Object linha [] = {tipoInfracao, tipoInfracao.getContextoTipoInfracao().getDescricao()};
            tbl.addRow(linha);
        }

        resetarTelaTipoInfracao();
    }
    
