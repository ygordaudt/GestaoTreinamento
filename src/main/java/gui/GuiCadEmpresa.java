    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        if (validarCampos()) {
            if (!alterando) {
                String nomeEmpresa = txtNome.getText();
                Empresa emp = new Empresa(nomeEmpresa);
                try {
                    dadosEmpresa.incluir(emp);
                    dadosAluno.alterar();
                    JOptionPane.showMessageDialog(null, "Empresa cadastrada com sucesso!", "Cadastro de Empresa", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Cadastro de Empresa", JOptionPane.ERROR_MESSAGE);
                }
                
            } else {
                empresa = (Empresa) tblEmpresas.getValueAt(tblEmpresas.getSelectedRow(), 0);
                empresa.setNomeEmpresa(txtNome.getText());
                
                try {
                    dadosEmpresa.alterar();
                    JOptionPane.showMessageDialog(null, "Cadastro alterado com sucesso!", "Cadastro de Empresa", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Cadastro de Empresa", JOptionPane.ERROR_MESSAGE);
                }
                
                //atualiza valores das equipes
                try {
                    equipes = dadosEquipe.getList();
                } catch (Exception ex) {
                    Logger.getLogger(GuiCadEmpresa.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                for (Equipe e : equipes) {
                    if (e.getEmpresa().equals(empresa)) {
                        e.setEmpresa(empresa);
                    }
                }
                
                try {
                    dadosEquipe.alterar();
                } catch (Exception ex) {
                    Logger.getLogger(GuiCadEmpresa.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                //atualizar valores dos alunos
                try {
                    alunos = dadosAluno.getList();
                } catch (Exception ex) {
                    Logger.getLogger(GuiCadEmpresa.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                for (Aluno al : alunos) {
                    if (al.getEmpresaColaborador().equals(empresa)) {
                        al.setEmpresaColaborador(empresa);
                        al.getEquipeAluno().setEmpresa(empresa);
                    }
                }
                
                try {
                    dadosAluno.alterar();
                } catch (Exception ex) {
                    Logger.getLogger(GuiCadEmpresa.class.getName()).log(Level.SEVERE, null, ex);
                }
                
                
            }
            
            preencherTabela();
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        try {
            this.empresa = (Empresa) tblEmpresas.getValueAt(tblEmpresas.getSelectedRow(), 0);
            
            int opcao
            = JOptionPane.showOptionDialog(null,
                "Confirma a exclusão?",
                "Pergunta",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE, null,
                new String[]{"Sim", "Não"}, "Sim");
            
            if (opcao == 0) {
            dadosEmpresa.remover(this.empresa);
            }
            
            preencherTabela();
            JOptionPane.showMessageDialog(rootPane, "Empresa removida com sucesso.");
            
        } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Por favor, selecione uma empresa.");

        }

    }//GEN-LAST:event_btnRemoverActionPerformed

