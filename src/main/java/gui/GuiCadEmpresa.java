    public void preencherTabela() {
        DefaultTableModel tbl = (DefaultTableModel) tblEmpresas.getModel();
        tblEmpresas.setRowSorter(new TableRowSorter(tbl));

        int qtdLinhas = tbl.getRowCount();
        for (int i = qtdLinhas - 1; i >= 0; i--) {
            tbl.removeRow(0);
        }
        
        List<Empresa> emps = new ArrayList<>();
        try {
            emps = dadosEmpresa.getList();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nenhuma empresa encontrada!", "Gestão de Turmas", JOptionPane.ERROR_MESSAGE);
        }
        
        for (int i = 0; i < emps.size(); i++) {
            Empresa emp = (Empresa) emps.get(i);
            Object linha [] = {emp};
            tbl.addRow(linha);
        }

        resetarTela();
    }
    
    public void resetarTela() {
        txtNome.setText("");
        txtNome.requestFocus();
        btnAlterar.setEnabled(true);
        btnAlterar.setSelected(false);
        btnAlterar.setText("Alterar");
        tblEmpresas.setEnabled(true);
        alterando = false;
    }
    
    public boolean validarCampos() {
        if(!(txtNome.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe o Nome da Empresa.");
            txtNome.requestFocus();
            return false;
        }
        return true;
    }

