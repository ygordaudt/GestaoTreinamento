    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        // TODO add your handling code here:
        if (validarCampos()) {
            if (!alterando) {
                String descricaoPercurso = txtDescricao.getText();
                tipoPercurso = new TipoPercurso (descricaoPercurso);
                try {
                    dadosTipoPercurso.incluir(tipoPercurso);
                    JOptionPane.showMessageDialog(null, "Percurso cadastrado com sucesso!", "Cadastro de Percurso", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Cadastro de Percurso", JOptionPane.ERROR_MESSAGE);
                }
                
            } else {
                tipoPercurso = (TipoPercurso) tblPercursos.getValueAt(tblPercursos.getSelectedRow(), 0);
                tipoPercurso.setDescricao(txtDescricao.getText());
                try {
                    dadosTipoPercurso.alterar();
                    JOptionPane.showMessageDialog(null, "Cadastro alterado com sucesso!", "Cadastro de Percurso", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception ex) {
                    JOptionPane.showMessageDialog(null, ex.getMessage(), "Cadastro de Percurso", JOptionPane.ERROR_MESSAGE);
                }
            }
            
            preencherTabela();
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

