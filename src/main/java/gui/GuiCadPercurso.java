    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        if (alterando) resetarTela();
        else {
            try {
                tipoPercurso = (TipoPercurso) tblPercursos.getValueAt(tblPercursos.getSelectedRow(), 0);
                String descricao = tipoPercurso.getDescricao();
                txtDescricao.setText(descricao);
                txtDescricao.requestFocus();
                txtDescricao.selectAll();
                alterando = true;
                btnAlterar.setText("Alterando");
                btnAlterar.setSelected(true);
                tblPercursos.setEnabled(false);
            } catch (Exception headlessException) {
                JOptionPane.showMessageDialog(null, "Por favor, selecione um percurso.");
                btnAlterar.setSelected(false);
            }
        }
    }//GEN-LAST:event_btnAlterarActionPerformed
    
    public void preencherTabela() {
        DefaultTableModel tbl = (DefaultTableModel) tblPercursos.getModel();
        tblPercursos.setRowSorter(new TableRowSorter(tbl));

        int qtdLinhas = tbl.getRowCount();
        for (int i = qtdLinhas - 1; i >= 0; i--) {
            tbl.removeRow(0);
        }

        List<TipoPercurso> tiposPercurso = new ArrayList();
        try {
           tiposPercurso = dadosTipoPercurso.getList();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, "Nenhum percurso encontrado!", "Cadastro de Percurso", JOptionPane.ERROR_MESSAGE);
        }
        
        for (int i = 0; i < tiposPercurso.size(); i++) {
            TipoPercurso tipoPercurso = (TipoPercurso) tiposPercurso.get(i);
            Object linha [] = {tipoPercurso, tipoPercurso.getDescricao()};
            tbl.addRow(linha);
        }

        resetarTela();
    }
    
    public void resetarTela() {
        txtDescricao.setText("");
        txtDescricao.requestFocus();
        btnAlterar.setEnabled(true);
        btnAlterar.setSelected(false);
        btnAlterar.setText("Alterar");
        tblPercursos.setEnabled(true);
    }
    
    public boolean validarCampos() {
        if(!(txtDescricao.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a Descrição do Percurso.");
            txtDescricao.requestFocus();
            return false;
        }
        return true;
    }

