    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        int opcao
        = JOptionPane.showOptionDialog(null,
            "Confirma a exclusão?",
            "Pergunta",
            JOptionPane.YES_NO_OPTION,
            JOptionPane.QUESTION_MESSAGE, null,
            new String[]{"Sim", "Não"}, "Sim");
        if (opcao == 0) {
            this.tipoPercurso = (TipoPercurso) tblPercursos.getValueAt(tblPercursos.getSelectedRow(), 0);
            try {
                dadosTipoPercurso.remover(this.tipoPercurso);
            } catch (Exception e) {
                JOptionPane.showMessageDialog(null, e.getMessage());
            }
            JOptionPane.showMessageDialog(rootPane, "Percurso removido com sucesso.");
            
            preencherTabela();
        }
    }//GEN-LAST:event_btnRemoverActionPerformed

