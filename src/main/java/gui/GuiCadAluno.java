    public boolean validarCampos() {
        if(cboEmpresa.getSelectedItem().equals(cboInicial)) {
            JOptionPane.showMessageDialog(null, "Selecione uma empresa", "Cadastro de Colaborador", JOptionPane.ERROR_MESSAGE);
            cboEmpresa.showPopup();
            return false;
        }
        
        if(cboFuncao.getSelectedItem().equals(cboInicial)) {
            JOptionPane.showMessageDialog(null, "Selecione uma função", "Cadastro de Colaborador", JOptionPane.ERROR_MESSAGE);
            cboFuncao.showPopup();
            return false;
        }
        
        if(!(txtMatricula.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe a matrícula do colaborador.", "Cadastro de Colaborador", JOptionPane.ERROR_MESSAGE);
            txtMatricula.requestFocus();
            return false;
        }
        
        if(!(txtNome.getText().length() > 0)) {
            JOptionPane.showMessageDialog(null, "Informe o nome do colaborador.", "Cadastro de Colaborador", JOptionPane.ERROR_MESSAGE);
            txtNome.requestFocus();
            return false;
        }
        
        if(!(rbtnAtivo.isSelected() || rbtnAfastado.isSelected() || rbtnDesligado.isSelected())){
            JOptionPane.showMessageDialog(null, "Selecione uma Situação.", "Cadastro de Colaborador", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        List<Aluno> alunos = new ArrayList<>();
        for (Aluno al : alunos) {
            if (txtMatricula.getText().equals(al.getMatricula().toString())) {
                JOptionPane.showMessageDialog(null, "O colaborador " + al.getNome() + " já se encontra cadastrado.", "Cadastro de Colaborador", JOptionPane.ERROR_MESSAGE);
                txtMatricula.requestFocus();
                return false;
            }
        }
        return true;
    }

