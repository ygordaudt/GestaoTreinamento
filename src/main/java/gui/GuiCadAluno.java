    private void btnRemoverColaboradorActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverColaboradorActionPerformed
        // TODO add your handling code here:
        try {
            Aluno al = (Aluno) tblColaboradores.getValueAt(tblColaboradores.getSelectedRow(), 0);

            int opcao
            = JOptionPane.showOptionDialog(null,
                "Confirma a exclusão?",
                "Pergunta",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE, null,
                new String[]{"Sim", "Não"}, "Sim");

            if (opcao == 0) {
                dadosAluno.remover(al);
                JOptionPane.showMessageDialog(rootPane, "Colaborador removido com sucesso.");
            }

            preencherTabela();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Por favor, selecione um colaborador.");
        }
    }//GEN-LAST:event_btnRemoverColaboradorActionPerformed

    private void btnAlterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAlterarActionPerformed
        // TODO add your handling code here:
        if (alterando || cadastrando) resetarTela();
        else {
            try {
                aluno = (Aluno) tblColaboradores.getValueAt(tblColaboradores.getSelectedRow(), 0);
                cboEmpresa.getModel().setSelectedItem(aluno.getEmpresaColaborador());
                cboFuncao.getModel().setSelectedItem(aluno.getFuncaoColaborador());
                txtMatricula.setText(aluno.getMatricula().toString());
                txtMatricula.setEnabled(false);
                txtNome.setText(aluno.getNome());
                txtNome.requestFocus();
                txtNome.selectAll();
                btnAlterar.setSelected(true);
                btnAlterar.setText("Alterando");
                btnGravar.setText("Consultar");
                btnCadastrar.setEnabled(false);
                btnGravar.setText("Gravar");
                btnRemoverColaborador.setEnabled(false);
                btnRemoverColaborador1.setEnabled(false);
                alterando = true;
                consultando = false;
                cadastrando = false;
                tblColaboradores.setEnabled(false);
                cboEmpresa.setEnabled(true);
                cboFuncao.setEnabled(true);
                txtNome.setEnabled(true);
                rbtnAtivo.setEnabled(true);
                rbtnAfastado.setEnabled(true);
                rbtnDesligado.setEnabled(true);
                if (null == aluno.getEstadoColaborador()) alterando = true;
                else switch (aluno.getEstadoColaborador()) {
                    case ATIVO:
                    rbtnAtivo.setSelected(true);
                    break;
                    case AFASTADO:
                    rbtnAfastado.setSelected(true);
                    break;
                    case DESLIGADO:
                    rbtnDesligado.setSelected(true);
                    default:
                    alterando = true;
                    break;
                }

            } catch (Exception headlessException) {
                JOptionPane.showMessageDialog(null, "Por favor, selecione um colaborador.");
                btnAlterar.setSelected(false);
            }
        }
    }//GEN-LAST:event_btnAlterarActionPerformed

    public void preencherTabela() {
        DefaultTableModel tbl = (DefaultTableModel) tblColaboradores.getModel();
        tblColaboradores.setRowSorter(new TableRowSorter(tbl));

        int qtdLinhas = tbl.getRowCount();
        for (int i = qtdLinhas - 1; i >= 0; i--) {
            tbl.removeRow(0);
        }
        
        List<Aluno> alunos = new ArrayList<>();
        try {
            alunos = dadosAluno.getList();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nenhum colaborador encontrado!", "Cadastro de Colaborador", JOptionPane.ERROR_MESSAGE);
        }

        for (Aluno al : alunos) {
            Object linha [] = {al, al.getEmpresaColaborador().getNomeEmpresa(), al.getFuncaoColaborador().getDescricaoFuncao(), al.getEstadoColaborador()};
            tbl.addRow(linha);
        }
        
        resetarTela();
    }
    
