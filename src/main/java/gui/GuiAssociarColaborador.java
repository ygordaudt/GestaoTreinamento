    private void btnAssociarUmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssociarUmActionPerformed
        // TODO add your handling code here:
        if (!lstNaoMatriculados.isSelectionEmpty()) {
            DefaultListModel lstNaoMat = (DefaultListModel) lstNaoMatriculados.getModel();
            DefaultListModel lstMat = (DefaultListModel) lstMatriculados.getModel();

            Aluno al = (Aluno) lstNaoMat.get(lstNaoMatriculados.getSelectedIndex());
            lstMat.addElement(al);
            lstNaoMat.removeElement(al);

            atualizarTotalizadores();
        }

    }//GEN-LAST:event_btnAssociarUmActionPerformed

