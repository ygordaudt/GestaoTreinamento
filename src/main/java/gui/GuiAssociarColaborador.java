    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        DefaultListModel lstNaoMat = (DefaultListModel) lstNaoMatriculados.getModel();
        DefaultListModel lstMat = (DefaultListModel) lstMatriculados.getModel();
        Equipe eq = (Equipe) cboEquipe.getSelectedItem();
        
        for (int i=0; i<lstNaoMat.getSize(); i++) {
            Aluno al = (Aluno) lstNaoMat.getElementAt(i);
            al.desmatricularAluno();
        }

        for (int i=0; i<lstMat.getSize(); i++) {
            Aluno al = (Aluno) lstMat.getElementAt(i);
            al.matricularAluno(eq);
        }

        try {
            dadosAluno.alterar();
            JOptionPane.showMessageDialog(null, "Associações realizadas com sucesso!", "Associação de Colaborador", JOptionPane.INFORMATION_MESSAGE);
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex.getMessage(), "Associação de Colaborador", JOptionPane.ERROR_MESSAGE);
        }
        
        resetarTela();
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnAssociarTodosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAssociarTodosActionPerformed
        // TODO add your handling code here:
        DefaultListModel lstNaoMat = (DefaultListModel) lstNaoMatriculados.getModel();
        DefaultListModel lstMat = (DefaultListModel) lstMatriculados.getModel();
        
        for (int i=0; i<lstNaoMat.getSize(); i++) {
            Aluno al = (Aluno) lstNaoMat.getElementAt(i);
            lstMat.addElement(al);
        }
        
        lstNaoMat.removeAllElements();
        atualizarTotalizadores();
    }//GEN-LAST:event_btnAssociarTodosActionPerformed

