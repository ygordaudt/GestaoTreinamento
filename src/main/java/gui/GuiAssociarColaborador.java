    public void resetarTela() {
        DefaultListModel lstNaoMat = (DefaultListModel) lstNaoMatriculados.getModel();
        DefaultListModel lstMat = (DefaultListModel) lstMatriculados.getModel();
        lstMat.removeAllElements();
        lstNaoMat.removeAllElements();
        
        
        consultando = false;
        lblEquipeSelecionada.setText("");
        lblTotalNaoMatriculados.setText("0");
        lblTotalMatriculados.setText("0");
        btnConsultar.setText("Consultar");
        btnGravar.setEnabled(false);
        cboEmpresa.setEnabled(true);
        cboEquipe.setEnabled(true);
        lstNaoMatriculados.setEnabled(false);
        lstMatriculados.setEnabled(false);
        chbTodasEmpresas.setEnabled(true);
        chbTodasEmpresas.setSelected(false);
        btnAssociarTodos.setEnabled(false);
        btnAssociarUm.setEnabled(false);
        btnDesassociarTodos.setEnabled(false);
        btnDesassociarUm.setEnabled(false);
        btnGravar.setEnabled(false);
        
        totalMatriculados = 0;
        totalNaoMatriculados = 0;
    }
    
    public void prepararConsulta() {
        DefaultListModel lstn = (DefaultListModel) lstNaoMatriculados.getModel();
        DefaultListModel lstm = (DefaultListModel) lstMatriculados.getModel();
        lstn.removeAllElements();
        lstm.removeAllElements();
        
        consultando = true;
        btnConsultar.setText("Cancelar");
        btnGravar.setEnabled(true);
        cboEmpresa.setEnabled(false);
        cboEquipe.setEnabled(false);
        lstNaoMatriculados.setEnabled(true);
        lstMatriculados.setEnabled(true);
        chbTodasEmpresas.setEnabled(false);
        btnAssociarTodos.setEnabled(true);
        btnAssociarUm.setEnabled(true);
        btnDesassociarTodos.setEnabled(true);
        btnDesassociarUm.setEnabled(true);

    }
    
    public void preencherCboEmpresa() {
        cboEmpresa.removeAllItems();
        cboEmpresa.addItem(cboInicialFiltrar);

        try {
            empresas = dadosEmpresa.getList();
        } catch (Exception e) {
            
        }
        for (Empresa emp: empresas) {
            cboEmpresa.addItem(emp);
        }
        
    }
    
    public void preencherCboEquipe(Empresa empresa) {
        cboEquipe.removeAllItems();
        cboEquipe.addItem(cboInicialSelecionar);

        try {
            equipes = dadosEquipe.getList();
        } catch (Exception e) {
            
        }
        
        for (Equipe eq : equipes) {
            if (eq.getEmpresa().equals(empresa)) cboEquipe.addItem(eq);
        }
    }
    
    public void atualizarTotalizadores() {
        DefaultListModel lstn = (DefaultListModel) lstNaoMatriculados.getModel();
        DefaultListModel lstm = (DefaultListModel) lstMatriculados.getModel();
        
        lblTotalNaoMatriculados.setText(Integer.toString(lstn.getSize()));
        lblTotalMatriculados.setText(Integer.toString(lstm.getSize()));
    }
    
    public boolean validarCampos() {
        if (cboEquipe.getSelectedItem()==cboInicialSelecionar) {
            JOptionPane.showMessageDialog(null, "Por favor, selecione uma equipe.", "Associação de Colaborador", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        return true;
    }
    

