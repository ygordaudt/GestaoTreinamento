    private void cboEmpresaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cboEmpresaItemStateChanged
        // TODO add your handling code here:
        if (evt.getStateChange() == ItemEvent.SELECTED && cboEmpresa.getSelectedItem()!=cboInicialFiltrar) { // Restringe execução apenas em caso de seleção manual no combobox
            Empresa emp = (Empresa) cboEmpresa.getSelectedItem();
            preencherCboEquipe(emp);

        }
        
        if (cboEmpresa.getSelectedItem().equals(cboInicialFiltrar)) {
            cboEquipe.removeAllItems();
            cboEquipe.addItem(cboInicialSelecionar);
        }
   
    }//GEN-LAST:event_cboEmpresaItemStateChanged

    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
        // TODO add your handling code here:
        if (consultando) {
            resetarTela();
        } else {
            if (validarCampos()) { //corrigir de acordo com o gui escala
                empresa = (Empresa) cboEmpresa.getSelectedItem();
                equipe = (Equipe) cboEquipe.getSelectedItem();
                DefaultListModel lstn = (DefaultListModel) lstNaoMatriculados.getModel();
                DefaultListModel lstm = (DefaultListModel) lstMatriculados.getModel();
                
                prepararConsulta();
                
                try {
                    alunos = dadosAluno.getList();
                } catch (Exception e) {
                }
                
                for (Aluno al : alunos) {
                    if (al.getEstadoAluno().equals(EstadoAluno.MATRICULADO)) {
                        lstm.addElement(al);
                    }
                }
                
                if (chbTodasEmpresas.isSelected()) {
                    for (Aluno al : alunos) {
                        if (al.getEstadoAluno().equals(EstadoAluno.NAO_MATRICULADO)) {
                            lstn.addElement(al);
                        }
                    }
                } else {
                    for (Aluno al : alunos) {
                        if (al.getEstadoAluno().equals(EstadoAluno.NAO_MATRICULADO)&&(al.getEmpresaColaborador().equals(empresa))) {
                            lstn.addElement(al);
                        }
                    }
                }

                lblEquipeSelecionada.setText(equipe.getDescricaoEquipe());
                atualizarTotalizadores();
            }
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

