    public void resetarTela() {
        DefaultListModel lstNaoMat = (DefaultListModel) lstNaoMatriculados.getModel();
        DefaultListModel lstMat = (DefaultListModel) lstMatriculados.getModel();
        lstMat.removeAllElements();
        lstNaoMat.removeAllElements();
        
        
        consultando = false;
        lblEquipeSelecionada.setText("");
        lblTotalNaoMatriculados.setText("0");
        lblTotalMatriculados.setText("0");
        btnConsultar.setText("Consultar");
        btnGravar.setEnabled(false);
        cboEmpresa.setEnabled(true);
        cboEquipe.setEnabled(true);
        lstNaoMatriculados.setEnabled(false);
        lstMatriculados.setEnabled(false);
        chbTodasEmpresas.setEnabled(true);
        chbTodasEmpresas.setSelected(false);
        btnAssociarTodos.setEnabled(false);
        btnAssociarUm.setEnabled(false);
        btnDesassociarTodos.setEnabled(false);
        btnDesassociarUm.setEnabled(false);
        btnGravar.setEnabled(false);
        
        totalMatriculados = 0;
        totalNaoMatriculados = 0;
    }
    
