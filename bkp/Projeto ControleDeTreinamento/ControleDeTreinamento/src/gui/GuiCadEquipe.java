    private void btnGravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGravarActionPerformed
        // TODO add your handling code here:
        if (validarCampos()) {
            Empresa emp = (Empresa) cboEmpresa.getSelectedItem();
            CorEquipe ce = (CorEquipe) cboCorEquipe.getSelectedItem();
            
            Equipe eq = new Equipe(emp, ce);
            try {
                dadosEquipe.incluir(eq);
                JOptionPane.showMessageDialog(null, "Equipe cadastrada com sucesso!", "Cadastro de Equipes", JOptionPane.INFORMATION_MESSAGE);
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex.getMessage(), "Cadastro de Equipes", JOptionPane.ERROR_MESSAGE);
            }

            preencherTabela();
        }
    }//GEN-LAST:event_btnGravarActionPerformed

    private void btnRemoverActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRemoverActionPerformed
        // TODO add your handling code here:
        try {
            equipe = (Equipe) tblEquipes.getValueAt(tblEquipes.getSelectedRow(), 0);
            
            int opcao
            = JOptionPane.showOptionDialog(null,
                "Confirma a exclusão?",
                "Pergunta",
                JOptionPane.YES_NO_OPTION,
                JOptionPane.QUESTION_MESSAGE, null,
                new String[]{"Sim", "Não"}, "Sim");
            
            if (opcao == 0) {
            dadosEquipe.remover(equipe);
            }
            
            preencherTabela();
            JOptionPane.showMessageDialog(rootPane, "Equipe removida com sucesso.");
            
        } catch (Exception e) {
                JOptionPane.showMessageDialog(null, "Por favor, selecione uma equipe.");

        }
            

            List<Aluno> alunos = new ArrayList<>();
            try {
                alunos = new DadosAluno().getList();
            } catch (Exception ex) {
                Logger.getLogger(GuiCadEquipe.class.getName()).log(Level.SEVERE, null, ex);
            }

            for (Aluno al : alunos) {
                if (al.getEquipeAluno().equals(equipe)) {
                    al.desmatricularAluno();
                }
            }
            
            try {
                new DadosAluno().alterar();
            } catch (Exception ex) {
                Logger.getLogger(GuiCadEquipe.class.getName()).log(Level.SEVERE, null, ex);
            } 

    }//GEN-LAST:event_btnRemoverActionPerformed

