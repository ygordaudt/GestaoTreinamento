    public void preencherTabela() {
        DefaultTableModel tbl = (DefaultTableModel) tblEquipes.getModel();
        tblEquipes.setRowSorter(new TableRowSorter(tbl));

        int qtdLinhas = tbl.getRowCount();
        for (int i = qtdLinhas - 1; i >= 0; i--) {
            tbl.removeRow(0);
        }
        
        List<Equipe> eqs = new ArrayList<>();
        try {
            eqs = dadosEquipe.getList();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Nenhuma equipe encontrada!", "Gestão de Turmas", JOptionPane.ERROR_MESSAGE);
        }
        
        for (int i = 0; i < eqs.size(); i++) {
            Equipe eq = (Equipe) eqs.get(i);
            Object linha [] = {eq, eq.getCorEquipe().getDescricao()};
            tbl.addRow(linha);
        }


    }
    
    public boolean validarCampos() {
        if(cboEmpresa.getSelectedItem().equals(cboInicial)) {
            JOptionPane.showMessageDialog(null, "Selecione uma empresa", "Cadastro de Equipe", JOptionPane.ERROR_MESSAGE);
            cboEmpresa.showPopup();
            return false;
        }
        
        if(cboCorEquipe.getSelectedItem().equals(cboInicial)) {
            JOptionPane.showMessageDialog(null, "Selecione uma cor para a equipe", "Cadastro de Equipes", JOptionPane.ERROR_MESSAGE);
            cboCorEquipe.showPopup();
            return false;
        }
        return true;
    }

